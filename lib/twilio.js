const { accountSid, authToken } = require('../config/environment');
const client = require('twilio')(accountSid, authToken);

function sendSMS(body, tel) {
  client.messages
    .create({
      body: body,
      from: '+441915803740',
      to: tel
    })
    .then(message => console.log(message.sid))
    .done();
}

module.exports = { sendSMS };
